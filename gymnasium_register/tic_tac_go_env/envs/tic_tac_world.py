                for j in range(0, len(board[i])):
                    if board[i][j] == "O" and self.oTwoX == -1:
                        self.oOneX = i
                        self.oOneY = j
                        self.initialOOneX = i
                        self.initialOOneY = j
                    if board[i][j] == "O" and self.oTwoX != -1:
                        self.oTwoX = i
                        self.oTwoY = j
