import gymnasium as gym
import pygame
import numpy as np
import importlib.util
from pathlib import Path
from typing import Optional

class TicTacWorldEnv(gym.Env):
    metadata = {"render_modes": ["human", "rgb_array"], "render_fps": 4}
    training_board_counts = {1: 15, 2: 30, 3: 50, 4: 75, 5: 75, 6: 75, 7: 75, 8: 75, 9: 50}
    training_boards_file = Path(__file__).resolve().parents[2] / "generated_training_boards.py"
    training_boards = None

    @classmethod
    def get_training_boards(cls):
        if cls.training_boards is not None:
            return cls.training_boards

        if cls.training_boards_file.exists():
            spec = importlib.util.spec_from_file_location("generated_training_boards", cls.training_boards_file)
            generated_boards = importlib.util.module_from_spec(spec)
            spec.loader.exec_module(generated_boards)
            cls.training_boards = generated_boards.TRAINING_BOARDS
            return cls.training_boards

        generator_path = Path(__file__).resolve().parents[2] / "board_generator.py"
        spec = importlib.util.spec_from_file_location("board_generator", generator_path)
        board_generator = importlib.util.module_from_spec(spec)
        spec.loader.exec_module(board_generator)

        generator = board_generator.BoardGenerator()
        cls.training_boards = {}
        for grad_num in cls.training_board_counts:
            count = cls.training_board_counts[grad_num]
            fixed_os = ((1, 1), (2, 1)) if grad_num == 1 else None
            cls.training_boards[grad_num] = generator.generate(
                grad=grad_num,
                count=count,
                fixed_os=fixed_os,
                seed=grad_num,
                verbose=False,
            )

        cls.save_training_boards()
        return cls.training_boards

    @classmethod
    def save_training_boards(cls):
        lines = [
            "# Auto-generated by TicTacWorldEnv. Delete this file to regenerate boards.\n",
            "TRAINING_BOARDS = {\n",
        ]
        for grad_num in sorted(cls.training_boards):
            lines.append(f"    {grad_num}: [\n")
            for board in cls.training_boards[grad_num]:
                lines.append("        (\n")
                for row in board:
                    lines.append(f"            {row!r},\n")
                lines.append("        ),\n")
            lines.append("    ],\n")
        lines.append("}\n")
        cls.training_boards_file.write_text("".join(lines))

